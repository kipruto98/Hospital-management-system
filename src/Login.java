    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String username = txtusername.getText();
        String password = txtpassword.getText();
        String utype = txtutype.getSelectedItem().toString();
        Connect();
        try {
            pst = con.prepareStatement("select * from user where username = ? and password = ? and usertype = ?");
            pst.setString(1, username);
            pst.setString(2, password);
            pst.setString(3, utype);
            rs = pst.executeQuery();
            if(rs.next()){
            int userid = rs.getInt("id");
            this.setVisible(false);
          new Main(userid,username,utype).setVisible(true);
            }
            else{
     new rojerusan.RSNotifyShadowFade("!ERROR!", "Username or Password do not match", Color.WHITE, Color.RED, 
   Color.BLACK, 4, RSNotifyShadowFade.PositionNotify.BottomRight, RSNotifyShadowFade.TypeNotify.ERROR).setVisible(true);
            }
        } catch (SQLException ex) {
                       Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
             JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

